package br.ufrgs.enq.gbflores;

public class Component {
	double compNumber;
	double molarVolume;
	double boilingPoint;
	double molarFraction;

	public double getCompNumber() {
		return compNumber;}
	
	public void setCompNumber(double compNumber) {
		this.compNumber = compNumber;}
	
	public double getMolarVolune() {
		return molarVolume;}
	
	public void setMolarVolune(double molarVolume) {
		this.molarVolume = molarVolume;}
	
	public double getBoilingPoint() {
		return boilingPoint;}
	
	public void setBoilingPoint(double boilingPoint) {
		this.boilingPoint = boilingPoint;}
	
	public double getMolarFraction() {
		return molarFraction;}
	
	public void setMolarFraction(double molarFraction) {
		this.molarFraction = molarFraction;}
	
}